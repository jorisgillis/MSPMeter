package msp;

/**
 * The one that makes progress with something
 * @author Joris Gillis
 */
public interface Progressor {
	
	/**
	 * Add a new ProgressListener to the Progressor.
	 * @param l	ProgressListener
	 */
	public void addProgressListener( ProgressListener l );
	
	/**
	 * Remove an existing ProgressListener from the Progressor
	 * @param l	ProgressListener
	 */
	public void removeProgressListener( ProgressListener l );
	
}
